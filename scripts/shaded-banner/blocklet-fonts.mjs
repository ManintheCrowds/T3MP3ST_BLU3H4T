/** @param {string} fontName */
export function getFont(fontName) {
  const shadow = STANDARD_SHADOW;
  if (fontName === 'standard_solid') {
    /** @type {Record<string, string[]>} */
    const solid = {};
    for (const [ch, rows] of Object.entries(shadow)) {
      solid[ch] = rows.map((line) =>
        [...line]
          .map((c) => (BOX_DRAW.has(c) ? ' ' : c))
          .join('')
      );
    }
    return solid;
  }
  return shadow;
}
